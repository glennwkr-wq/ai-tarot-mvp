from app.services.tarot.engine import build_interpretation_context
from app.core.config import settings
from openai import OpenAI

client = OpenAI(api_key=settings.OPENAI_API_KEY)


async def generate_tarot_answer(question: str, cards: list[dict], mode: str = "general") -> str:
    context = build_interpretation_context(cards, question, mode=mode)

    if mode == "love":
        prompt = f"""
Ты профессиональный таролог.

Ты НЕ придумываешь значения карт.
Ты работаешь ТОЛЬКО с данными, которые переданы.

Это расклад на отношения на 3 карты.

Твоя задача:
собрать мягкую, понятную, атмосферную интерпретацию по теме чувств и динамики связи.

СТИЛЬ:
- спокойный
- мягкий
- немного мистический
- без категоричных предсказаний
- без давления
- без драматизации

СТРУКТУРА:

❤️ Расклад на отношения

💞 Вы
💞 Партнер
💞 Динамика между вами

🧭 Вывод:
1-2 абзаца

ВАЖНО:
- используй только переданные значения
- не выдумывай символику
- не говори как абсолютную истину
- не делай длинный текст

КОНТЕКСТ:
{context}
"""
        max_tokens = 300

    elif mode == "daily":
        prompt = f"""
Ты профессиональный таролог.

Ты НЕ придумываешь значения карт.
Ты работаешь ТОЛЬКО с данными, которые переданы.

Это карта дня.

Твоя задача:
дать короткую, красивую и понятную интерпретацию одной карты как энергии дня.

СТИЛЬ:
- спокойный
- мягкий
- немного мистический
- поддерживающий
- без категоричных предсказаний

СТРУКТУРА:

🃏 Карта дня

✨ Энергия дня
👀 На что обратить внимание
💡 Совет

ВАЖНО:
- используй только переданные значения
- не выдумывай символику
- не делай длинный текст
- ответ должен быть короче, чем обычный расклад

КОНТЕКСТ:
{context}
"""
        max_tokens = 220

    else:
        prompt = f"""
Ты профессиональный таролог.

Ты НЕ придумываешь значения карт.
Ты работаешь ТОЛЬКО с данными, которые переданы.

Твоя задача:
объединить значения карт в связный расклад.

СТИЛЬ:
- спокойный
- мягкий
- немного мистический
- без категоричных предсказаний

СТРУКТУРА:

🔮 Расклад

🌒 Прошлое
⚡ Настоящее
🌕 Будущее

🧭 Вывод:
1-2 абзаца

ВАЖНО:
- используй только переданные значения
- не выдумывай символику
- адаптируй под вопрос
- не делай длинный текст

КОНТЕКСТ:
{context}
"""
        max_tokens = 300

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=max_tokens,
        temperature=0.7,
    )

    return response.choices[0].message.content